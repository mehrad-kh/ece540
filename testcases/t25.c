int cascading(int n)
{
  int c1,c2,c3,c4,c5,c6,c7,c8,c9,i;

  if (c1) {
    if (c2) {
      if (c3) {
        if (c4) {
          if (c5) {
            if (c6) {
              if (c7) {
                if (c8) {
                  if (c9) {
                    for (i=0;i<n;i++) {
                      c1=c2=c3=c4=c5=c6=c7=c8=c9=0;
                    }
                  }
                }
              }
            }
          }
        }
      }
    }
  }
  return i;
}
