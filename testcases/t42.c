int cascading(int n)
{
  int c1,c2,c3,c4,c5,c6,c7,c8,c9;
  int i1,i2,i3,i4,i5,i6,i7,i8,i9,i;

  for (i1=0;i1<c1;i1++) {
    for (i2=0;i2<c2;i2++) {
      for (i3=0;i3<c3;i3++) {
        for (i4=0;i4<c4;i4++) {
          for (i5=0;i5<c5;i5++) {
            for (i6=0;i6<c6;i6++) {
              for (i7=0;i7<c7;i7++) {
                for (i8=0;i8<c8;i8++) {
                  for (i9=0;i9<c9;i9++) {
                    for (i=0;i<n;i++) {
                      c1=c2=c3=c4=c5=c6=c7=c8=c9=0;
                    }
                  }
                }
              }
            }
          }
        }
      }
    }
  }
  return i;
}
